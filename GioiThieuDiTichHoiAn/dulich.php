	<img src="image/chuachucthanh.jpg" class="gioithieu_img">
	<p class="gioithieu_p">Chùa Chúc Thánh tọa lạc tại Cẩm Phô, Hội An, tỉnh Quảng Nam, là nơi khai sinh ra chi phái Thiền Chúc Thánh của Thiền Lâm Tế. Theo sách Việt Nam Phật giáo sử luận của Nguyễn Lang, chùa Chúc Thánh đã được Thiền sư Minh Hải - Pháp Bảo khai sơn vào thế kỷ XVII. Vị Thiền sư này người tỉnh Phước Kiến, sang nước ta vào thời Chúa Nguyễn Phúc Trân (1687 - 1691). Ông đã tự giới đàn tại chùa Linh Mụ, sau đó vào Hội An dựng chùa Chúc Thánh. Dần dần ngôi chùa này trở nên một ngôi tổ đình lớn ở miền Trung và miền Nam.  </p>
